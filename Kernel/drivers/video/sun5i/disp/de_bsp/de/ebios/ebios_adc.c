__s32 ADC_Write_bits(__u32 reg_offset, __u32 bits_offset, __u32 bits_num, __u32 dst_value)
{
    __u32 reg;
    __u32 mask;
    
    if ((dst_value > (1<<bits_num)) || (bits_offset > 31) || (bits_num > 32))
    {
        return -1;
    }
    
    reg   = ADC_RUINT32(reg_offset);
    mask  = ((1<<bits_num) - 1)<<bits_offset;
    reg  &= ~mask;
    mask  = dst_value << bits_offset;
    reg  |= mask;
    ADC_WUINT32(reg_offset, reg);
    
    return 0;
}

__u32 ADC_Read_bits(__u32 reg_offset, __u32 bits_offset, __u32 bits_num)
{
    __u32 reg;
    __u32 mask;
    
    if ((bits_offset > 31) || (bits_num > 32))
    {
        return -1;
    }
    
    reg   = ADC_RUINT32(reg_offset);
    mask  = ((1<<bits_num) - 1)<<bits_offset;
    reg  &= mask;
    reg >>= bits_offset;
    
    return reg;
}

