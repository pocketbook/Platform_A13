__u32 ADC_lookup_temperature(__u32 resistance, __u32* R_table, __u32 R_table_item_num)
{
	__u32 i = 0;
	__u32 temp0, temp1;
    
	for(i = 0; i < R_table_item_num; i++)
	{
		if(resistance >= R_table[i])
		{	
			break;
		}	
	}
	if(i == 0 || i == R_table_item_num - 1)
	{
		return i;
	}
	else if(i == R_table_item_num)
	{
		return i - 1;
	}
	else
	{
		temp0 = resistance -  R_table[i];
		temp1 = R_table[i-1] - resistance;
        
        //__wrn("---temp0 = %d, temp1= %d,i = %d, (%d, %d)\n",temp0,temp1,i,R_table[i],R_table[i-1]);
		
		if(temp0 > temp1)
		{
			return i+1;
		}else
		{
			return i;
		}	
	}	
}

